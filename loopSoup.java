package yabo;

public class loopSoup {

	public static void main(String[] args) {
		int i = 5;
		int j;
		while (i > 0) {
		    System.out.println(i);
		    i--;
		    j = 3;
		    while (j > 0) {
		        System.out.print(j);
		        j--;
		    }
		    System.out.println();
		}

	}

}
